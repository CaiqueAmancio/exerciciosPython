"""
Peça ao usuário para digitar três valores inteiros e imprima a soma deles
"""

print('Olá, por favor digite três números inteiros e direi a soma deles')
num1 = int(input(f'Digite o primeiro número inteiro: '))
num2 = int(input(f'Digite o segundo número inteiro: '))
num3 = int(input(f'Digite o terceiro número inteiro: '))
# soma = num1 + num2 + num3
print(f'A soma dos três números é: {num1 + num2 + num3}')
